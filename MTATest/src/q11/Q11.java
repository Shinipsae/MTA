package q11;

public class Q11 {
	// 프로그램이 최소량의 메모리를 사용하여 데이터를 저장하게 할때
	// 알맞은 데이터 형식을 고르시오.
	public static void main(String[] args) {
		short birthyear = 1974; // int 아님
		boolean exempt = false;
		char initial = 'D';
		float salary = 22123.5f;
		
		System.out.println(birthyear); // 1974
		System.out.println(exempt); // false
		System.out.println(initial); // D
		System.out.println(salary); // 22123.5
	}

}




